namespace HoardeGame.Gameplay.Gems
{
    /// <summary>
    /// Information about gem contents
    /// </summary>
    public class GemInfo
    {
        /// <summary>
        /// Gets or sets the amount of red gems
        /// </summary>
        public int RedGems { get; set; }

        /// <summary>
        /// Gets or sets the amount of green gems
        /// </summary>
        public int GreenGems { get; set; }

        /// <summary>
        /// Gets or sets the amount of blue gems
        /// </summary>
        public int BlueGems { get; set; }

        /// <summary>
        /// Gets or sets the amount of keys
        /// </summary>
        public int Keys { get; set; }
    }
}